import java.util.Scanner;

public class CHFIDEAL {
	public static void main(String[] args)
	{
		Scanner scan = new Scanner(System.in);
		System.out.println(1);
		
		int num = scan.nextInt();
		
		if(num == 2)
			System.out.println(3);
		else
			System.out.println(2);
		
		scan.close();
		System.out.flush();
	}
}
